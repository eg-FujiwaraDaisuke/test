import 'package:minden/features/power_plant/domain/entities/power_plant_detail.dart';

class PowerPlantDetailModel extends PowerPlantDetail {
  const PowerPlantDetailModel({
    required String plantId,
    required String areaCode,
    required String name,
    required String viewAddress,
    required String voltageType,
    required String powerGenerationMethod,
    required String renewableType,
    required double generationCapacity,
    required int displayOrder,
    required bool isRecommend,
    required String ownerName,
    required DateTime startDate,
    required DateTime endDate,
    String? projectName,
    String? url,
    String? ownerMessage,
    String? aboutPlant,
    String? prospect,
    String? twitter,
    String? facebook,
    required String plantImage1,
    String? plantImage2,
    String? plantImage3,
    String? plantImage4,
    String? limitedIntroducerId,
    String? giftName,
    String? giftDescription,
    String? giftImage,
    String? shortCatchphrase,
    String? catchphrase,
    String? thankYouMessage,
    String? thankYouImage,
  }) : super(
          plantId: plantId,
          areaCode: areaCode,
          name: name,
          viewAddress: viewAddress,
          voltageType: voltageType,
          powerGenerationMethod: powerGenerationMethod,
          renewableType: renewableType,
          generationCapacity: generationCapacity,
          displayOrder: displayOrder,
          isRecommend: isRecommend,
          ownerName: ownerName,
          startDate: startDate,
          endDate: endDate,
          projectName: projectName,
          url: url,
          ownerMessage: ownerMessage,
          aboutPlant: aboutPlant,
          prospect: prospect,
          twitter: twitter,
          facebook: facebook,
          plantImage1: plantImage1,
          plantImage2: plantImage2,
          plantImage3: plantImage3,
          plantImage4: plantImage4,
          limitedIntroducerId: limitedIntroducerId,
          giftName: giftName,
          giftDescription: giftDescription,
          giftImage: giftImage,
          shortCatchphrase: shortCatchphrase,
          catchphrase: catchphrase,
          thankYouMessage: thankYouMessage,
          thankYouImage: thankYouImage,
        );

  factory PowerPlantDetailModel.fromJson(Map<String, dynamic> json) {
    return PowerPlantDetailModel(
      plantId: json['powerPlant']['plantId'],
      areaCode: json['powerPlant']['areaCode'],
      name: json['powerPlant']['name'],
      viewAddress: json['powerPlant']['viewAddress'],
      voltageType: json['powerPlant']['voltageType'],
      powerGenerationMethod: json['powerPlant']['powerGenerationMethod'],
      renewableType: json['powerPlant']['renewableType'],
      generationCapacity: json['powerPlant']['generationCapacity'],
      displayOrder: json['powerPlant']['displayOrder'],
      isRecommend: json['powerPlant']['isRecommend'],
      ownerName: json['powerPlant']['ownerName'],
      startDate: DateTime.parse(json['powerPlant']['startDate']),
      endDate: DateTime.parse(json['powerPlant']['endDate']),
      projectName: json['powerPlant']['projectName'],
      url: json['powerPlant']['url'],
      ownerMessage: json['powerPlant']['ownerMessage'],
      aboutPlant: json['powerPlant']['aboutPlant'],
      prospect: json['powerPlant']['prospect'],
      twitter: json['powerPlant']['twitter'],
      facebook: json['powerPlant']['facebook'],
      plantImage1: json['powerPlant']['plantImage1'],
      plantImage2: json['powerPlant']['plantImage2'],
      plantImage3: json['powerPlant']['plantImage3'],
      plantImage4: json['powerPlant']['plantImage4'],
      limitedIntroducerId: json['powerPlant']['limitedIntroducerId'],
      giftName: json['powerPlant']['giftName'],
      giftDescription: json['powerPlant']['giftDescription'],
      giftImage: json['powerPlant']['giftImage'],
      shortCatchphrase: json['powerPlant']['shortCatchphrase'],
      catchphrase: json['powerPlant']['catchphrase'],
      thankYouMessage: json['powerPlant']['thankYouMessage'],
      thankYouImage: json['powerPlant']['thankYouImage'],
    );
  }
}
